package com.example.finalandroidnc;

public class DataMaps {
    public String name;
    public String a;
    public String b;

    public DataMaps() {
    }

    public DataMaps(String name, String a, String b) {
        this.name = name;
        this.a = a;
        this.b = b;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
